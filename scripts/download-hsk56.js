fs.mkdirSync(HSK_DIR, { recursive: true });

function writeHSK(level, words) {
  const entries = words.map(w => ({ simplified: w }));
  const file = path.join(HSK_DIR, `hsk${level}.json`);
  // Don't overwrite if the file already has richer data
  if (fs.existsSync(file)) {
    const existing = JSON.parse(fs.readFileSync(file, 'utf8'));
    if (existing.length > 0 && existing[0].forms) {
      console.log(`HSK ${level}: ${file} already has rich data (${existing.length} entries) — skipping`);
      return;
    }
  }
  fs.writeFileSync(file, JSON.stringify(entries, null, 2));
  console.log(`HSK ${level}: wrote ${entries.length} entries to ${file}`);
}

